#include <iostream>
using namespace std;

int main(){
	int x = 20;
	int y = 12;
	int z = 9;
	
	cout<<"x + y + z = ";
	cout<<x + y + z;
	
	cout<<"x - y - z = ";
	cout<<x - y -z;
	
	cout<<"x * y * z =";
	cout<<x * y *z;
	
	cout<<"x / y / z =";
	cout<<x / y / z;
	
	cout<<"x + y / z - z = ";
	cout<< x + y / z - z;
	
	return 0;
}
